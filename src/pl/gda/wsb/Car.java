package pl.gda.wsb;

public class Car {

    final String marka;
    final String model;
    String nrRejestracyjny ;
    String kolor;


    public Car(String marka, String model, String nrRejestracyjny, String kolor) {
        this.marka = marka;
        this.model = model;
        this.nrRejestracyjny = nrRejestracyjny;
        this.kolor = kolor;
    }
}
